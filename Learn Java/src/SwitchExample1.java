
public class SwitchExample1 {
 public static void main(String[]args) {
	 int num=23;
	 switch(num) {
	 case 10:
		 System.out.println("10");
		 break;
	 case 20:
		 System.out.println("20");
		 break;
	 case 30:
		 System.out.println("30");
	default:
		System.out.println("nothing");
	 
	 }
	 
	 
 }
}
