package Encaptualtions;

public class Person {
	private String name;
	private int age;
	
	public void setName(String newName) {
		this.name=newName;
	}
	
	public void setAge(int newAge) {
		this.age=newAge;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
}

 
